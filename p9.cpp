#include <iostream>
using namespace std;
 
int main()
{
    int intT;
    char charT;
    float floatT;
    double doubleT;
 
    
    
    cout << "Size of int is: " <<
    sizeof(intT) <<endl;
 
    
    cout << "Size of char is: " <<
    sizeof(charT) <<endl;
     

    cout << "Size of float is: " <<
    sizeof(floatT) <<endl;
 
    cout << "Size of double is: " <<
    sizeof(doubleT) <<endl;
 
    return 0;
}
